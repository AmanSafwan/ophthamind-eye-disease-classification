function h(string $value): string
{
    return htmlspecialchars($value, ENT_QUOTES, 'UTF-8');
}

function detect_app_url(): string
{
    $scheme = (!empty($_SERVER['HTTPS']) && $_SERVER['HTTPS'] !== 'off') ? 'https' : 'http';
    $host = $_SERVER['HTTP_HOST'] ?? 'localhost';
    $script = $_SERVER['SCRIPT_NAME'] ?? '/hosting_setup.php';
    $dir = str_replace('\\', '/', dirname($script));
    $dir = rtrim($dir, '/');
    if ($dir === '' || $dir === '.') {
        return $scheme . '://' . $host;
    }
    return $scheme . '://' . $host . $dir;
}

function write_env_file(string $path, array $values): bool
{
    $lines = [
        '# OphthaMind — production (.env generated by hosting_setup.php)',
        'APP_NAME=OphthaMind AI',
        'APP_ENV=production',
        'APP_DEBUG=false',
        'APP_URL=' . ($values['app_url'] ?? ''),
        '',
        'DB_HOST=' . ($values['db_host'] ?? 'localhost'),
        'DB_PORT=' . ($values['db_port'] ?? '3306'),
        'DB_NAME=' . ($values['db_name'] ?? ''),
        'DB_USER=' . ($values['db_user'] ?? ''),
        'DB_PASS=' . ($values['db_pass'] ?? ''),
        '',
        'SESSION_TIMEOUT=3600',
        'SESSION_NAME=eye_system_session',
        '',
        'UPLOAD_DIR=upload/',
        'MAX_UPLOAD_SIZE=5242880',
        'ALLOWED_TYPES=jpg,jpeg,png',
        '',
        'ENABLE_AI=false',
        'ENABLE_REGISTRATION=false',
        '',
    ];

    return file_put_contents($path, implode(PHP_EOL, $lines)) !== false;
}

function test_db(array $values): PDO
{
    $host = $values['db_host'] ?? 'localhost';
    $port = $values['db_port'] ?? '3306';
    $name = $values['db_name'] ?? '';
    $user = $values['db_user'] ?? '';
    $pass = $values['db_pass'] ?? '';

    if ($name === '' || $user === '') {
        throw new RuntimeException('Database name and user are required.');
    }

    $dsn = "mysql:host={$host};port={$port};dbname={$name};charset=utf8mb4";
    return new PDO($dsn, $user, $pass, [
        PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
    ]);
}

function import_sql_file(PDO $pdo, string $sqlPath): void
{
    if (!is_file($sqlPath)) {
        throw new RuntimeException('SQL file not found.');
    }

    $sql = file_get_contents($sqlPath);
    if ($sql === false || trim($sql) === '') {
        throw new RuntimeException('SQL file is empty.');
    }

    $pdo->exec('SET FOREIGN_KEY_CHECKS=0');
    $pdo->exec('SET NAMES utf8mb4');

    $buffer = '';
    $lines = preg_split('/\R/', $sql) ?: [];
    foreach ($lines as $line) {
        $trim = trim($line);
        if ($trim === '' || str_starts_with($trim, '--') || str_starts_with($trim, '/*')) {
            continue;
        }
        $buffer .= $line . "\n";
        if (substr(rtrim($line), -1) === ';') {
            $pdo->exec($buffer);
            $buffer = '';
        }
    }

    if (trim($buffer) !== '') {
        $pdo->exec($buffer);
    }

    $pdo->exec('SET FOREIGN_KEY_CHECKS=1');
}

