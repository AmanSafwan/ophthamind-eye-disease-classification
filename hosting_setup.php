        'db_user' => trim((string) ($_POST['db_user'] ?? '')),
        'db_pass' => (string) ($_POST['db_pass'] ?? ''),
        'import_sql' => isset($_POST['import_sql']) ? '1' : '0',
    ];
    $testOnly = isset($_POST['test_only']);

    try {
        $pdo = test_db($values);

        if ($testOnly) {
            $tableCheck = $pdo->query("SHOW TABLES LIKE 'users'")->fetchColumn();
            if ($tableCheck) {
                $success = 'Connection OK — users table found. Click "Save & finish setup" below.';
            } elseif ($sqlFile) {
                $success = 'Connection OK — database is empty. Tick "Import database" then click "Save & finish setup".';
            } else {
                $success = 'Connection OK — but users table is missing. Import eye_system_hosting.sql in phpMyAdmin first.';
            }
            $defaults = array_merge($defaults, $values);
        } else {
            if (!write_env_file($envFile, $values)) {
                throw new RuntimeException('Could not write .env file. Check folder permissions.');
            }

            if ($values['import_sql'] === '1' && $sqlFile) {
                import_sql_file($pdo, $sqlFile);
                $success = 'Database imported and configuration saved.';
            } else {
                $tableCheck = $pdo->query("SHOW TABLES LIKE 'users'")->fetchColumn();
                if (!$tableCheck) {
                    throw new RuntimeException('Table "users" not found. Import eye_system_hosting.sql in phpMyAdmin, or tick "Import database".');
                }
                $success = 'Configuration saved. Existing database detected.';
            }

            if (!is_dir($basePath . '/storage')) {
                @mkdir($basePath . '/storage', 0755, true);
            }
            if (!is_dir($basePath . '/storage/logs')) {
                @mkdir($basePath . '/storage/logs', 0755, true);
            }
            if (!is_dir($basePath . '/upload')) {
                @mkdir($basePath . '/upload', 0755, true);
            }

            file_put_contents($lockFile, date('c') . PHP_EOL);
            $step = 'done';
        }
    } catch (Throwable $e) {
        $error = $e->getMessage();
        $defaults = array_merge($defaults, $values);
    }
}

